// Arithmetic Operators
let a = 10;
let b = 5;

console.log(a+b);
console.log(a-b);
console.log(a*b);
console.log(a/b);
console.log(a%b);
console.log(++a);
console.log(--b);

// Assignment Operators
let num = 10;
num += 5;                 
console.log(num);         

let text = "Hello";
text += " world";         
console.log(text);        

// Comparison Operators
let x = 5;
let y = 10;

console.log(x == y);       
console.log(x != y);      
console.log(x < y);        
console.log(x > y);       
console.log(x <= y);       
console.log(x >= y);       

// Logical Operators
let p = 5;
let q = 2;

console.log(p>3 && q>3);       
console.log(p>3 || q>3);       
console.log(!(p>3));           

// Bitwise Operators
let m = 5;  // Binary representation is 101
let n = 3;  // Binary representation is 011

console.log(m & n);  
console.log(m | n);  
console.log(~m);     

let x1 = 10;
y = x1++;
console.log(x1);
console.log(y);

let x2 = 10;
 y1 = ++x2 
console.log(x2);
console.log(y1);